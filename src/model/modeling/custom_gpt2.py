class GPT2GatedMLP(nn.Module):
    def __init__(self, intermediate_size, config: CustomGPT2Config):
        super().__init__()
        embed_dim = config.hidden_size
        self.c_fc_0 = Conv1D(intermediate_size, embed_dim)
        self.c_fc_1 = Conv1D(intermediate_size, embed_dim)
        self.c_proj = Conv1D(embed_dim, intermediate_size)
        self.act = ACT2FN[config.activation_function]
        self.dropout = nn.Dropout(config.resid_pdrop)

    def forward(self, hidden_states: Optional[Tuple[torch.FloatTensor]]) -> torch.FloatTensor:
        hidden_act = self.act(self.c_fc_0(hidden_states))
        hidden_linear = self.c_fc_1(hidden_states)
        hidden_states = hidden_act * hidden_linear
        hidden_states = self.c_proj(hidden_states)
        hidden_states = self.dropout(hidden_states)
        return hidden_states


