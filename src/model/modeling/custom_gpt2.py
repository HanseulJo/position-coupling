class CustomGPT2Block(nn.Module):
    def __init__(self, config: CustomGPT2Config, layer_idx=None):
        super().__init__()
        hidden_size = config.hidden_size
        inner_dim = config.n_inner if config.n_inner is not None else 4 * hidden_size
        if config.normalization_layer == 'layernorm':
            norm = nn.LayerNorm
        elif config.normalization_layer == 'rmsnorm':
            norm = RMSNorm
        else:
            raise ValueError(f"{config.normalization_layer} is not implemented normalization layer."
                             "Useable options: ['layernorm', 'rmsnorm'].")
        self.norm_pos = config.normalization_position

        self.ln_1 = norm(hidden_size, eps=config.layer_norm_epsilon)
        self.ln_2 = norm(hidden_size, eps=config.layer_norm_epsilon)
        if self.norm_pos == 'pre_post':
            self.ln_1_1 = norm(hidden_size, eps=config.layer_norm_epsilon)
            self.ln_2_1 = norm(hidden_size, eps=config.layer_norm_epsilon)
        self.attn = CustomGPT2Attention(config, layer_idx=layer_idx)
        

        if config.is_gated_act:
            self.mlp = GPT2GatedMLP(inner_dim, config)
        else:
            self.mlp = GPT2MLP(inner_dim, config)

    def forward(
        self,
        hidden_states: Optional[Tuple[torch.FloatTensor]],
        layer_past: Optional[Tuple[torch.Tensor]] = None,
        attention_mask: Optional[torch.FloatTensor] = None,
        head_mask: Optional[torch.FloatTensor] = None,
        use_cache: Optional[bool] = False,
        output_attentions: Optional[bool] = False,
    ) -> Union[Tuple[torch.Tensor], Optional[Tuple[torch.Tensor, Tuple[torch.FloatTensor, ...]]]]:
        residual = hidden_states
        if self.norm_pos in ['pre', 'pre_post']:
            hidden_states = self.ln_1(hidden_states)
        attn_outputs = self.attn(
            hidden_states,
            layer_past=layer_past,
            attention_mask=attention_mask,
            head_mask=head_mask,
            use_cache=use_cache,
            output_attentions=output_attentions,
        )
        attn_output = attn_outputs[0]  # output_attn: a, present, (attentions)
        outputs = attn_outputs[1:]
        # residual connection
        hidden_states = attn_output + residual
        if self.norm_pos == 'post':
            hidden_states = self.ln_1(hidden_states)
        elif self.norm_pos == 'pre_post':
            hidden_states = self.ln_1_1(hidden_states)

        residual = hidden_states
        if self.norm_pos in ['pre', 'pre_post']:
            hidden_states = self.ln_2(hidden_states)
        feed_forward_hidden_states = self.mlp(hidden_states)
        # residual connection
        hidden_states = residual + feed_forward_hidden_states
        if self.norm_pos == 'post':
            hidden_states = self.ln_2(hidden_states)
        elif self.norm_pos == 'pre_post':
            hidden_states = self.ln_2_1(hidden_states)

        if use_cache:
            outputs = (hidden_states,) + outputs
        else:
            outputs = (hidden_states,) + outputs[1:]

        return outputs  # hidden_states, present, (attentions, cross_attentions)


